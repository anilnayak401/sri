/**
 * Flat sorted list of all diseases — used for full-text search.
 * Built once at module load time, never rebuilt.
 */
export const ALL_DISEASES = Object.values(DISEASES_BY_LETTER)
  .flat()
  .sort((a, b) => a.localeCompare(b))

/**
 * getByLetter(letter)
 * Returns all diseases starting with the given letter.
 * Simulates a 200ms async delay — replace with a real fetch() call
 * when a backend API is available.
 *
 * @param {string} letter — single uppercase letter A–Z
 * @returns {Promise<string[]>}
 */
export async function getByLetter(letter) {
  // Simulate network latency (remove when using real API)
  await new Promise(r => setTimeout(r, 200))
  return DISEASES_BY_LETTER[letter.toUpperCase()] ?? []
}

/**
 * searchDiseases(query)
 * Full-text search across all diseases.
 * Simulates a 150ms async delay — replace with a real fetch() call.
 *
 * @param {string} query — free-text search string
 * @returns {Promise<string[]>}
 */
export async function searchDiseases(query) {
  await new Promise(r => setTimeout(r, 150))
  const q = query.toLowerCase().trim()
  if (!q) return []
  return ALL_DISEASES.filter(d => d.toLowerCase().includes(q))
}
